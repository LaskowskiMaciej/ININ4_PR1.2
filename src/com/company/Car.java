package com.company;

public class Car {
    final String model;
    final String producer;
    final String name;
    final int year;
    String color;

    public Car(String model, String producer, String name, int year, String color) {
        this.model = model;
        this.producer = producer;
        this.name = name;
        this.year = year;
        this.color = color;
    }

}
